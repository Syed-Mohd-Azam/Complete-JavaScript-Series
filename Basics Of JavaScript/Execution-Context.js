// Execution Context

// Everything in JavaScript happens inside the Execution Context.Execution Context is like a container and it contains two components --> Variable Environment and Thread of Execution.

// Variable Environment --> This is the environment where all the variables and functions of a program are stored. Here, all the variables and functions are stored in the key value pairs.

// Code Environment --> This is the environment where the code of a program is executed. This component is also called as Thread of Execution.
