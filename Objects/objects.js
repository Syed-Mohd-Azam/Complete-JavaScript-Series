// In JavaScript, an object is an unordered collection of key value pairs.Each key-value pair is called property.
// The key of a property can be a string whereas the value of a property can be any value example string, number, array, object, function etc.
// JavaScript provides you many ways to  create an object and commonly used one is object literal.

var person = {
  firstName: "Syed",
  middleName: "Mohd",
  lastName: "Azam",
  age: 28,
};

// Here person is an object whereas the firstName,middleName,lastName,age are the keys and "Syed", "Mohd","Azam", 28 are the values.
