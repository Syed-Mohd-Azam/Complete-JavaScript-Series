var values = [12, 45, 67, 89];

console.log(values.pop());
console.log(values);

// pop() method removes and returns the last element from array.

console.log(values.push(100)); // 4 total number of new elements.
console.log(values);

// push() method adds a new Element and returns the total number of new elements.

console.log(values.shift());
console.log(values);

// shift() method returns and removes  the very first elemnt from the array.
